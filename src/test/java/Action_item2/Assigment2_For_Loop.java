package Action_item2;

public class Assigment2_For_Loop {
    public static void main(String[] args) {

        //iterate through all cities defined by dynamic array using for loop
        String[] cities = new String[4];
        cities[0] = "Brooklyn";
        cities[1] = "Queens";
        cities[2] = "Manhattan";
        cities[3] = "Bronx";

        for(int i =0; i < cities.length;i++){
            //print statement
            System.out.println(cities[i]);

        }//end of for loop



    }//end of main
}//end of java class
